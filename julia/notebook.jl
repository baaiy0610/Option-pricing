function gamma1(tau, x_max, q)
    BC = zeros(size(tau,1),1)
    return BC
end

# ╔═╡ 090098c1-f7e7-4f7b-b21b-9cae74e76aa7
function gamma2(tau, x_max, q)
    BC = exp.(0.5*(q+1)*x_max .+ 0.25*(q+1)^2*tau) .- exp.(0.5*(q-1)*x_max .+ 0.25*(q-1)^2*tau)
    return BC
end

# ╔═╡ c98802c0-f7b1-4463-a6eb-071fa9e88eff
function u0(x, q)
    IC = max.(exp.((q+1)*x/2).-exp.((q-1)*x/2),0)
    return IC
end

# ╔═╡ fd7768dd-5f30-481a-b824-c2bf660ed96b
function heatmatrix1d(M,r)
	A=zeros(M,M)
	for i=1:M
		A[i,i]=1+2*r
	end
	for i=2:M
		A[i,i-1]=-r
	end
	for i=1:M-1
		A[i,i+1]=-r
	end
	return A
end

# ╔═╡ 15fb5efa-54dd-4812-8e57-601cd39b345c
function transfer(u, M, N, K, x, tau, q)
	v =zeros(N+1, M+2)
	for i=1:N+1
		for j=1:M+2
			v[i,j] = K*u[i,j]*exp((-0.5*(q-1))*x[j] + (-0.25*(q+1)^2)*tau[i])
		end
	end	
	return v
end

# ╔═╡ 2968aec3-6234-4029-9dd2-3a3522bf701c
function CN_FDM(M, N, S_min, S_max, T, K, sigma, r)
	#transfer the Time and Stock
	TAU = 0.5 * sigma^2 * T
	tau = range(TAU, 0, length=N+1)
	delta_tau = TAU / N
	x_min = log(S_min/K)
	x_max = log(S_max/K)
	x = range(x_min, x_max, length=M+2)
	delta_x = (x_max - x_min) / M
	l = delta_tau / delta_x^2
	q= (2*r) / sigma^2
	#initialize the boundary condition and initial condition
	u = zeros(N+1, M+2)
	u[N+1, :] = u0(x, q)
	u[:, 1] = gamma1(tau, x[1], q)
	u[:, M+2] = gamma2(tau, x[M+2], q)
	A = heatmatrix1d(M, l)
	for i=N:-1:1
		b = zeros(M, 1)
		b[1] = u[i, 1]
		b[M] = u[i, M+2]
		u[i,2:M+1] = inv(A) * (u[i+1, 2:M+1] + l*b)
	end
	#transfer the u back to v
	v = transfer(u, M, N, K, x, tau, q)
	return v
end

